    public void hienThiTB() {
        for (GiaoTiep g : al) {
            DoiTuongClient t = (DoiTuongClient) g;

            table.addRow(new Object[]{
                t.getIp(), t.getPort(), t.getUser(), t.getTrangThai()
            });
        }
    }

    public void them(DoiTuongClient tb) {

        al.add(tb);
        IOFile iOFile = new IOFile();
        iOFile.ghi(al, "client.dat");
        table.addRow(new Object[]{
            tb.getIp(), tb.getPort(), tb.getUser(), tb.getTrangThai()
        });
    }

    public void ngatKetNoi() {
        int r = jTable1.getSelectedRow();
        if (r != -1) {
            try {
                table.removeRow(r);
                al.remove(r);
                iOFile.ghi(al, "client.dat");
                PrintStream ps = new PrintStream(al_s.get(r).getOutputStream());

                ps.println("server đã ngắt kết nối");
                al_s.get(r).close();
                DSDoiTuongClient.remove(r);

                System.out.println("da tat ket noi!");
            } catch (IOException ex) {
                Logger.getLogger(QLDSClient.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Hay chon hang ma ban muon xoa!");
        }
    }

    public void xoaClient(Socket client) {
        int i = 0;
        for (GiaoTiep x : al) {
            DoiTuongClient v = (DoiTuongClient) x;
            if (v.getIp().equals(client.getInetAddress().toString()) && v.getPort().equals(String.valueOf(client.getPort()))) {
                al.remove(x);
                iOFile.ghi(al, "client.dat");
                table.removeRow(i);
                System.out.println("xoa thanh cong!");
            }
            i++;
        }

    }

